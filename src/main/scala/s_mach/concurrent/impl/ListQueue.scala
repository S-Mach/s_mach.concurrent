package s_mach.concurrent.impl

/**
 * A trait for queue that uses an unlimited size linked list as the backend
 * for the queue.
 */
trait ListQueue[A] extends Queue[A]

object ListQueue {
  def apply[A](elems: A*) : ListQueue[A] = new ListQueueImpl[A](elems:_*)
}